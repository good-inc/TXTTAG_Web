<p><b>Notes:</b><ul>
	<li>You may work either individually, or in teams.</li>
	<li>BYOL: Bring your own laptop.</li>
	<li>Files and necessities will be provided. (Don’t scrape the website)</li>
	<li>iOS HTML5 Web Apps are OK. (Add to Homescreen)</li>
	<li>Starting from free templates is OK.</li>
	<li>Submission does not require an app icon, though it doesn’t hurt to include one.</li>
	<li>The site is written in PHP, using a MySQL database.</li>
	<li>Have your IDE of choice ready to go.<br><i>I personally recommend <a href="https://developer.apple.com/xcode/" target="_blank">X Code</a> for iOS, <a href="http://www.jetbrains.com/idea/download/" target="_blank">IntelliJ IDEA</a> for Android <br>and either <a href="http://panic.com/coda/" target="_blank">Coda</a>, or <a href="http://notepad-plus-plus.org/" target="_blank">Notepad++</a> for the web interface.</i></li>
	</ul></p>
	
<p><b>Grading Sheet:</b> (0-10 scale)<ul>
<li>Design Completion (Visual)<br>
<i>Were all of the elements of your walkthrough included?</i></li>
<li>Design Completion (Functional)<br>
<i>Were all of TXT TAG’s existing features included?</i></li>
<li>UI Creativity<br>
<i>How does it compare to the most beautiful websites and apps?</i></li>
<li>UI Intuition<br>
<i>Is it immediately clear how to use the app?</i></li>
<li>Compatibility<br>
<i>Does it work and look good in all browsers / on all Android devices?</i></li>
<li>Ad placement<br>
<i>Is it always visible, yet unobtrusive?</i></li>
<li>Design of Email message<br>
<i>Less important, leaving it untouched nets you 5/10.</i></li>
<li>Speed</li>
<li>Filesize</li>
<li>Attractiveness</li>
<li>Fun Level</li>
</ul></p>

