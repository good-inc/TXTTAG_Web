<p style="text-align: justify; text-indent:40px;">We need <b><i>graphic designers</i></b> experienced in Photoshop or Illustrator, <b><i>programmers</i></b> experienced in HTML, PHP or CSS, and <b><i>creative individuals</i></b> to help us create iOS, Android and Web interfaces atop our working backend code.</p>
<p style="text-align: justify; text-indent:40px;">It's okay if you can't create an entire UI by yourself, show up to the event and form a team! You may win $500, and at the very least you get free pizza.</p>


<p><b>Mission:</b> Create appealing UI’s around working code.</p>

<p><b>Prizes:</b> $500 cash for best iOS, Android, and browser-based designs. ($500 each)</p>

<p><b>Food:</b> Free pizza, soda, energy drinks & maybe more!</p>

<p><b>Start:</b> Saturday, February 16, 2013 @ 12p (for ~24 hours)</p>

<p><b>End:</b> Sunday, February 17, 2013 @ 10a (Demos begin)</p>

<p><b>Venue:</b><blockquote>
	Gainesvillle’s Hackerspace<br>
	101 SE 2nd Place, Ste. 100<br>
	Gainesville, FL 32601<br>
	In the basement of the downtown Sun Center, check <a href="http://www.skillhouse.org" target="_blank">http://skillhouse.org</a> for more info.</blockquote></p>

<p><b>Submission Requirements:</b><ul>
	<li>Must have a UI walkthrough sketched on paper before coding.</li>
	<li>Must use completely free software solutions. (ie. no app-builders or licensed code)</li>
	<li>Must focus on the UI; this is not about adding features.</li>
	<li>Must include instructions to add a new static page. (ie. for announcements or new features)</li>
	</ul></p>
	
<p><b>Judges:</b> (Including successful Gainesville entrepreneurs)<ul>
